  React.ElementRef<typeof TabsPrimitive.Trigger>,
  React.ComponentPropsWithoutRef<typeof TabsPrimitive.Trigger>
>(({ className, ...props }, ref) => (
  <TabsPrimitive.Trigger
    ref={ref}
    className={cn(
      'inline-flex items-center justify-center whitespace-nowrap rounded-lg px-4 py-2 text-sm font-medium',
      'transition-all duration-200',
      'data-[state=active]:bg-[var(--card)] data-[state=active]:text-[var(--foreground)]',
      'data-[state=active]:shadow-[0_1px_3px_rgba(15,23,42,0.08)]',
      'hover:text-[var(--foreground)]',
      className
    )}
    {...props}
  />
));
TabsTrigger.displayName = TabsPrimitive.Trigger.displayName;

